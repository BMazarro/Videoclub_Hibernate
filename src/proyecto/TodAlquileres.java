     public void mostrarTabla(Session s){
        limpiar();
        Alquiler a= new Alquiler();
        Query q = s.createQuery("from Alquiler order by fechaalq");
        Iterator<Alquiler> iter= q.iterate();
        Object[] fila= new Object[6];
        java.text.SimpleDateFormat sdf=new java.text.SimpleDateFormat("yyyy-MM-dd");
        int i=0;
        while (iter.hasNext()){
                a= (Alquiler) iter.next();
                AlquilerId ai= a.getId();
                fila[0]= ai.getIdcli();
                fila[1]= ai.getIdpel();
                fila[2]= ai.getNum();
                String fechaalq= sdf.format(ai.getFechaalq());
                fila[3]= fechaalq ;
                fila[4]= agregarDias(ai.getFechaalq(),3);
                fila[5]= a.getFechadev();
        //        fila[5]= a.getImporte();
            //System.out.println(""+a);
            modelo.addRow(fila);
        }
}
