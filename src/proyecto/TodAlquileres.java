    public void ponerDisponible(int idpel, int pelnum,Session s){
        Ejemplares c= new Ejemplares();
        Query q = s.createQuery("from Ejemplares where idpel="+idpel+ " and num="+pelnum);
        Iterator<Ejemplares> iter= q.iterate();
        while(iter.hasNext()){
            c= (Ejemplares) iter.next();
            c.setEstado("Disponible");
        } 
    }
